public class Kehu {
    private int id;
    private int zichan;

    public Kehu(int id, int zichan) {
        this.id = id;
        this.zichan = zichan;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getZichan() {
        return zichan;
    }

    public void setZichan(int zichan) {
        this.zichan = zichan;
    }
}
